// 변수 기본 출력

public class Ex2_1 {

	public static void main(String[] args) {
		System.out.println(3+5);
		System.out.println(3-5);
		System.out.println(3*5);
		System.out.println(3/5.0);
		System.out.println(3/5);
	}
}

// print: 출력 후 줄바꿈 없음.
// println: 출력 후 줄바꿈.
// "" 안은 문자로 인식하여 적은 내용 그대로 보여줌
// 연산은 ""없이 그대로 적는다